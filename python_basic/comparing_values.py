# Comparing values in Python
x = 10
y = 20

print(x > y)    # False
print(x < y)    # True
print(x >= y)   # False
print(x <= y)   # True
print(x != y)   # True
print(x == y)   # False
print(type(x == y))  # <class 'bool'>
print(type(x < y))   # <class 'bool'>
print("c" == "c")    # True
print("c" == "d")    # False
print("c" > "d")     # False
print("c" < "d")     # True
print(type("c" < "f"))  # <class 'bool'>
